<script>
import {registerAut} from "@/utils/api"
export default {
  name: "authorRegister",
  data() {
    var checkRegName = (rule, value, callback) => {
      if (!value) {
        return callback(new Error("笔名不能为空"));
      } else if (/^[\u4E00-\u9FA5A-Za-z0-9]+$/.test(value) == false) {
        callback(new Error("请不要输入特殊符号"));
      } else {
        callback();
      }
    };

    var checkQQ = (rule, value, callback) => {
      if (!value) {
        return callback(new Error("QQ不能为空"));
      } else {
        callback();
      }
      setTimeout(() => {
        if (!Number.isInteger(value)) {
          callback(new Error("请输入数字值"));
        } else {
          if (value < 5) {
            callback(new Error("QQ为5~12位数字"));
          } else if (value > 12) callback(new Error("QQ为5~12位数字"));
          else {
            callback();
          }
        }
      }, 1000);
    };
    var checkPhone = (rule, value, callback) => {
      if (!value) {
        return callback(new Error("手机号码不能为空"));
      } else if (
        /^(13[0-9]|14[0-9]|15[0-9]|16[6]|18[0-9]|19[6,9]|17[0-9])\d{8}$/i.test(
          value
        ) == false
      ) {
        callback(new Error("请输入正确的手机号"));
      } else {
        callback();
      }
      setTimeout(() => {
        if (!Number.isInteger(value)) {
          callback(new Error("请输入数字值"));
        } else {
          if (value != 11) {
            callback(new Error("手机号为11位数字"));
          } else {
            callback();
          }
        }
      }, 1000);
    };

    var checkCheck = (rule, value, callback) => {
      if (!value) {
        return callback(new Error("验证码不能为空"));
      } else {
        callback();
      }
    };

    var validatePass = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请输入密码"));
      } else {
        if (this.ruleForm.checkPass !== "") {
          this.$refs.ruleForm.validateField("checkPass");
        }
        callback();
      }
    };
    var validatePass2 = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请再次输入密码"));
      } else if (value !== this.ruleForm.pass) {
        callback(new Error("两次输入密码不一致!"));
      } else {
        callback();
      }
    };
    return {
      userInfo: {},
      active: 1,
      ruleForm: {
        regName: "",
        qq: "",
        phone: "",
        check: "",
        pass: "",
        checkPass: "",
      },
      rules: {
        regName: [{ validator: checkRegName, trigger: "blur" }],
        qq: [{ validator: checkQQ, trigger: "blur" }],
        phone: [{ validator: checkPhone, trigger: "blur" }],
        pass: [{ validator: validatePass, trigger: "blur" }],
        checkPass: [{ validator: validatePass2, trigger: "blur" }],
        check: [{ validator: checkCheck, trigger: "blur" }],
      },
    };
  },
  methods: {
    back() {
      this.active = 1;
    },
    submitForm(formName) {
      this.$refs[formName].validate((valid) => {
        if (valid) {
          const reqInfo ={
            userId:this.userInfo.userId,
            userPic:this.userInfo.userPic,
            regName:this.ruleForm.regName,
            qq:this.ruleForm.qq,
            phone:this.ruleForm.phone,
            pass:this.ruleForm.pass,
            check:this.ruleForm.check,
          }
               registerAut(reqInfo);
      this.$router.push("/authorLogin");

        } else {
          console.log("error submit!!");
          return false;
        }
      });
    },
    resetForm(formName) {
      this.$refs[formName].resetFields();
    },
  },

  created() {
    var str = localStorage.getItem("userInfo");
    this.userInfo = JSON.parse(str);
  },
};
</script>

